package com.mythic.db.entities;

import java.io.Serializable;

public class SettingLogin implements Serializable {

	private String name;
	private String value;

	public SettingLogin() {
	}

	public SettingLogin(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return this.value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
